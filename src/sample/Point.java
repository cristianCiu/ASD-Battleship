package sample;
public class Point {
	
	private int xPosition;
	private int yPosition;
	
	
	public setYPosition(int yPosition) {
		this.yPosition = yPosition;
	}
	
	public setXPosition(int xPosition) {
		this.xPosition = xPosition;
	}
	
	public getYPosition() {
		return this.yPosition;
	}
	
	public getXPosition() {
		return this.xPosition;
	}
	
	
	
}
